    def change_sequence(self,df):
        """
          将DataFrame中每10000行的7000到10000行数据移动到末尾。

          Args:
            df: pandas DataFrame。

          Returns:
            修改后的DataFrame。
          """
        # 将DataFrame转换成numpy数组
        data = df.to_numpy()

        # 计算行数和列数
        num_rows, num_cols = data.shape

        # 计算能够完整切分成10000行组的行数
        num_rows_to_process = num_rows // 10000 * 10000

        # 只对能够完整切分的部分进行操作
        data_to_process = data[:num_rows_to_process]

        # 将数据reshape成10000行一组的二维数组
        reshaped_data = data_to_process.reshape(-1, 10000, num_cols)

        # 切片每组的最后3000行
        rows_to_move = reshaped_data[:, 7000:8000, :].reshape(-1, num_cols)
        rows_to_move2 = reshaped_data[:, 8000:, :].reshape(-1, num_cols)

        # 删除原数据
        remaining_data = reshaped_data[:, :7000, :].reshape(-1, num_cols)

        # 合并数据
        new_data = np.concatenate([remaining_data, rows_to_move], axis=0)
        new_data = np.concatenate([new_data, rows_to_move2], axis=0)

        # 添加剩余的行
        new_data = np.concatenate([new_data, data[num_rows_to_process:]], axis=0)

        # 转换回DataFrame
        new_df = pd.DataFrame(new_data, columns=df.columns)

        return new_df



